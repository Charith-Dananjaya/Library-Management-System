    private void AdminloginbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AdminloginbtnActionPerformed
         PreparedStatement ps;
        ResultSet rs;
        String uname = adminunamefield.getText();
        String pass = String.valueOf(adminpasswordfield1.getPassword());
        
        String query = "SELECT * FROM `admin_registration` WHERE `admin_name` =? AND `admin_password` =?";
        
        try {
            ps = SQLCON.connect().prepareStatement(query);
            
            ps.setString(1, uname);
            ps.setString(2, pass);
            
            rs = ps.executeQuery();
            
            if(rs.next())
            {
                    this.dispose();
                    AdminMenue1 adminM = new AdminMenue1();
                    adminM.setVisible(true);
                    
                    
                    
                    
            }
            else{
                    JOptionPane.showMessageDialog(null, "Incorrect Username Or Password", "Login Failed", 2);
                }
            
        } catch (SQLException ex) {
            Logger.getLogger(AdminLogin1.class.getName()).log(Level.SEVERE, null, ex);
        }
                                     
    }//GEN-LAST:event_AdminloginbtnActionPerformed

