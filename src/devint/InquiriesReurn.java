    private void InqsubmitbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_InqsubmitbtnActionPerformed
       String inqno = jTextField3.getText();
        String idno = jTextField4.getText();
        String email = jTextField2.getText();
       // String bookstatus = bookStatus.getText();
        String message = jTextArea1.getText();
        
        if (inqno.equals(""))
        {
            JOptionPane.showMessageDialog(null, "Fill Required fields");
        }
        else if (idno.equals(""))
        {
            JOptionPane.showMessageDialog(null, "Fill Required fields");
        }
        else if (email.equals(""))
        {
            JOptionPane.showMessageDialog(null, "Fill Required fields");
        }
        else if (message.equals(""))
        {
            JOptionPane.showMessageDialog(null, "Fill Required fields");
        }
        
        else
        {
            String sql = "INSERT INTO inquiry_table (inq_no, member_id, email, inq_message) VALUES (?,?,?,?)";
            try
            {
                pst = conn.prepareStatement(sql);
                
                pst.setString(1, jTextField3.getText());
                pst.setString(2, jTextField4.getText());
                pst.setString(3, jTextField2.getText());
               // pst.setString(3, bookStatus.getText());
                pst.setString(4, jTextArea1.getText());
                
                pst.execute(); 
                JOptionPane.showMessageDialog(null, "Successfully added!!");
            }
            catch(Exception e)
            {
                JOptionPane.showMessageDialog(null, e);
            }
            jTextField3.setText(null);
            jTextField4.setText(null);
            jTextField2.setText(null);
            jTextArea1.setText(null);
            
        } 
    }//GEN-LAST:event_InqsubmitbtnActionPerformed
