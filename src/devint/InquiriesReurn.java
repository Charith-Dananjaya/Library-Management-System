private void jTable1Load(){
        int c;
        try {
            pst = conn.prepareStatement("select inq_no, member_id, email, inq_message, Status from inquiry_table;");
            rs = pst.executeQuery();
           
            ResultSetMetaData rsd = rs.getMetaData();
            c = rsd.getColumnCount();
            d = (DefaultTableModel)jTable1.getModel();
            d.setRowCount(0);
           
//** While loop for load the data
            while(rs.next())
            {
              Vector v2 = new Vector();
              for(int i=1; i<=c; i++)
              {
              v2.add(rs.getString("inq_no"));
              v2.add(rs.getString("member_id"));
              v2.add(rs.getString("email"));
              v2.add(rs.getString("Status"));
              v2.add(rs.getString("inq_message"));
              
                                     
              }
             d.addRow(v2);
            }
             
        } catch (SQLException ex) {
            Logger.getLogger(InquiriesReurn.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
